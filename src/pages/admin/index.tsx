export const getServerSideProps: GetServerSideProps = async (context) => {
  const session = await getServerSession(context.req, context.res, authOptions);

  if (session?.user.role === "Admin") {
    let allMemberships,
      allConsumptionsByCategories,
      allPromotions,
      allConsumptions,
      allUsers,
      usersCount;

    try {
      allConsumptionsByCategories = await fetchAllConsumptionsByCategories();
    } catch (err) {
      console.error(err);
    }

    try {
      allMemberships = await prisma.membership.findMany({
        orderBy: { minPoints: "asc" },
      });
    } catch (err) {
      console.log(err);
    }

    try {
      allPromotions = await fetchAllPromotions();

      allConsumptions = await prisma.consumptionCategory.findMany({
        select: { consumptions: { select: { name: true } } },
      });
    } catch (err) {
      console.log(err);
    }

    try {
      allUsers = await prisma.user.findMany({
        skip: 0,
        take: 20,
      });

      usersCount = await prisma.user.count();
    } catch (err) {
      console.log(err);
    }

    return {
      props: {
        allMemberships,
        allConsumptionsByCategories,
        allPromotions,
        allConsumptions,
        allUsers,
        usersCount,
      },
    };
  }

  return {
    redirect: {
      destination: "/",
      permanent: false,
    },
  };
};
