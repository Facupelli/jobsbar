type InputData = {
  id: string;
};

function LoginInput() {
  const {
    register,
    watch,
    formState: { errors },
    setFocus,
    reset,
    setValue,
  } = useForm<InputData>();
  const router = useRouter();

  const [submitError, setError] = useState<string>("");

  const id = watch("id");

  const { mutate } = api.user.getUserById.useMutation();

  const onSubmit = useCallback(
    async (id: string) => {
      mutate(
        { id },
        {
          onSuccess: (data) => {
            console.log(data);
            if (data?.success) {
              router.push(`/user/${id}`);
            }
            reset({ id: "" });
          },
          onError: (err) => {
            console.log("catch", err);
            setValue("id", "");
          },
        }
      );
    },
    [reset, router, setValue]
  );

  useEffect(() => {
    if (id?.length === 25) {
      onSubmit(id);
    }
  }, [id, onSubmit]);

  useEffect(() => {
    setFocus("id");
  }, [setFocus, submitError]);

  return (
    <>
      {/* {submitError && !!submitError && (
        <Modal
          isOpen={!!submitError}
          handleCloseModal={() => setError("")}
          error
        >
          <p>{submitError}</p>
        </Modal>
      )} */}
      <form>
        <section>
          <input
            type="password"
            {...register("id")}
            autoFocus
            role="id-input"
          />
        </section>
      </form>
    </>
  );
}
